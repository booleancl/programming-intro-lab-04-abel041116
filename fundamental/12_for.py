# El for es una estructura de control que nos permite realizar accion, pero con elementos diferentes cadaves

veggies = ['broccoli', 'celery', 'lettuce', 'carrot']

for veggie in veggies:
    print('I need to eat more',veggie)

fruits = ['apple', 'Strawber','banana','orange']

for fruit in fruits:
    print('I love to eat', fruit)